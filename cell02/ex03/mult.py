#!/usr/bin/env python3

def main():
    # ให้ผู้ใช้กรอกตัวเลขสองตัว
    num1 = float(input("กรุณากรอกตัวเลขแรก: "))
    num2 = float(input("กรุณากรอกตัวเลขที่สอง: "))

    # คูณตัวเลขทั้งสอง
    result = num1 * num2

    # เช็คผลลัพธ์ว่าบวก ลบ หรือศูนย์
    if result > 0:
        print("ผลลัพธ์เป็นบวก.")
    elif result < 0:
        print("ผลลัพธ์เป็นลบ.")
    else:
        print("ผลลัพธ์เป็นศูนย์.")

    # แสดงผลลัพธ์ของการคูณ
    print(f"ผลลัพธ์ของการคูณ {num1} และ {num2} คือ: {result}")

# ให้โปรแกรมทำงานเมื่อถูกเรียกใช้งาน
if __name__ == "__main__":
    main()
